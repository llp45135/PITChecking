        try {
            String strLicense = editLicense.getText();
            
            int ret = SDKMethod.INSTANCE.setActivation(ConvertToByteArray(strLicense));
            if(ret != SDKMethod.SDK_NO_ERROR) {
                JOptionPane.showMessageDialog(null, "Activation Failed!", "Warning", JOptionPane.PLAIN_MESSAGE);
                return;
            }
            
            FileOutputStream out = new FileOutputStream("license.txt");
            out.write(editLicense.getText().getBytes());
            out.close();
            
            JOptionPane.showMessageDialog(null, "Activation Success!", "Information", JOptionPane.PLAIN_MESSAGE);
        } catch (Exception e){            
        }
        
