    public static byte[] ConvertToByteArray(String str) {
        byte[] bytes = new byte[str.length() + 1];
        Arrays.fill(bytes, (byte)0);
        System.arraycopy(str.getBytes(), 0, bytes, 0, str.getBytes().length);
        
        return bytes;
    }

    public static String fromByteArray(byte[] array) {
        int realLen = 0;

        for (int i = 0; i < array.length; i++) {
            if (array[i] != 0)
                realLen++;
            else
                break;
        }

        byte[] realStr = new byte[realLen];
        System.arraycopy(array, 0, realStr, 0, realLen);

        return new String(realStr);
    }

